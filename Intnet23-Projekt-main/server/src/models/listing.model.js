class Listing {
  constructor(id, name, email, username, description, price, img, buyer) {
    this.id = id;
    this.name = name;
    this.email = email;
    this.username = username;
    this.description = description;
    this.price = price;
    this.img = img;
    this.buyer = buyer;
  }
}

export default Listing;
